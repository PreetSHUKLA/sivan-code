class EliteCodingAgentOrchestrator:
    def __init__(self, config: APIConfig, max_iterations: int = 3, max_redesigns: int = 2, timeout_per_agent: int = 60, audit_log_file: str = "agent_audit.jsonl"):
        self.config = config
        self.max_iterations = max_iterations
        self.max_redesigns = max_redesigns
        self.timeout = timeout_per_agent

        self.audit_logger = AuditLogger(audit_log_file)
        self.cost_metrics = CostMetrics()

        self.planner = PlannerAgent(config, self.audit_logger)
        self.worker = WorkerAgent(config, self.audit_logger)
        self.reviewer = ReviewerAgent(config, self.audit_logger)

    def execute_task(self, task_description: str, codebase_context: str = "") -> Dict:
        print("ELITE AI CODING AGENT")
        print("=" * 80)
        print(f"Task: {task_description}")
        print(f"Session: {self.audit_logger.session_id}\n")
        redesign_count = 0
        plan_data = None

        while redesign_count <= self.max_redesigns:
            if redesign_count == 0:
                print("PHASE 1: PLANNING\n")
                plan_data = self.planner.create_plan(task_description, codebase_context)
                self.cost_metrics.add_cost("planner", 0, plan_data["cost"])

            code = None
            review_result = None
            previous_severity = Severity.NONE

            for iteration in range(1, self.max_iterations + 1):
                print(f"ITERATION {iteration}/{self.max_iterations}")
                print("-" * 80 + "\n")

                priority_fixes = review_result.get('critical_issues') if review_result else None
                feedback = review_result.get('feedback') if review_result and not review_result['approved'] else None

                code, worker_cost = self.worker.write_code(plan_data, feedback, iteration, priority_fixes)
                self.cost_metrics.add_cost("worker", 0, worker_cost)

                review_result = self.reviewer.review_code(code, task_description, iteration)
                self.cost_metrics.add_cost("reviewer", 0, review_result["cost"])

                if review_result['approved']:
                    print("=" * 80)
                    print("APPROVED - PRODUCTION READY!")
                    print("=" * 80)
                    self._print_cost_summary()
                    return self._build_result(code, review_result, iteration, redesign_count, plan_data, True, "SUCCESS")

                elif review_result['needs_redesign']:
                    print("=" * 80)
                    print("CRITICAL ARCHITECTURAL ISSUE")
                    print("=" * 80)

                    redesign_feedback = f"SEVERITY: {review_result['severity'].name}\nCRITICAL: {review_result['critical_issues']}"
                    self.audit_logger.log_redesign(redesign_count + 1, review_result['critical_issues'])
                    plan_data = self.planner.create_plan(task_description, codebase_context, redesign_feedback)
                    self.cost_metrics.add_cost("planner", 0, plan_data["cost"])
                    redesign_count += 1
                    break

                previous_severity = review_result['severity']

        self._print_cost_summary()
        return self._build_result(code, review_result, self.max_iterations, redesign_count, plan_data, False, "FAILED")

    def _build_result(self, code, review_result, iterations, redesigns, plan_data, approved, status):
        return {
            "final_code": code, "review": review_result,
            "iterations_used": iterations, "redesigns_used": redesigns,
            "plan": plan_data['plan'], "approved": approved, "status": status,
            "cost_metrics": asdict(self.cost_metrics), "audit_log": self.audit_logger.log_file
        }

    def _print_cost_summary(self):
        print("\n" + "=" * 80)
        print("COST SUMMARY")
        print("=" * 80)
        print(f"Total: ${self.cost_metrics.total_cost:.6f}")
        print(f"Planner: ${self.cost_metrics.planner_cost:.6f}")
        print(f"Worker: ${self.cost_metrics.worker_cost:.6f}")
        print(f"Reviewer: ${self.cost_metrics.reviewer_cost:.6f}")
        print(f"Tokens: {self.cost_metrics.total_tokens:,}")
        print("=" * 80)


