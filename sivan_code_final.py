    def _extract_and_validate_code(self, raw_code: str) -> tuple[bool, str, str]:
        """Extracts Python code from markdown and validates its syntax using AST."""
        clean_code = raw_code

        # 1. Strip Markdown formatting
        if "```python" in raw_code:
            try:
                start = raw_code.find("```python") + 9
                end = raw_code.find("```", start)
                if end != -1:
                    clean_code = raw_code[start:end].strip()
            except Exception:
                pass
        elif "```" in raw_code:
            try:
                start = raw_code.find("```") + 3
                end = raw_code.find("```", start)
                if end != -1:
                    clean_code = raw_code[start:end].strip()
            except Exception:
                pass

        clean_code = clean_code.strip()

        # 2. Validate Syntax
        try:
            ast.parse(clean_code)
            return True, clean_code, ""
        except SyntaxError as e:
            error_msg = f"SyntaxError on line {e.lineno}: {e.msg}\nCode snippet: {e.text}"
            return False, clean_code, error_msg

