class AuditLogger:
    """Persistent audit logging"""

    def __init__(self, log_file: str = "agent_audit.jsonl"):
        self.log_file = log_file
        self.session_id = hashlib.md5(str(datetime.now()).encode()).hexdigest()[:8]

    def log(self, event_type: str, data: dict):
        entry = {
            "timestamp": datetime.now().isoformat(),
            "session_id": self.session_id,
            "event_type": event_type,
            "data": data
        }
        with open(self.log_file, "a") as f:
            f.write(json.dumps(entry) + "\n")

    def log_api_call(self, agent: str, model: str, prompt_length: int, response_length: int, cost: float):
        self.log("api_call", {
            "agent": agent,
            "model": model,
            "prompt_length": prompt_length,
            "response_length": response_length,
            "cost": cost
        })

    def log_iteration(self, iteration: int, status: str, severity: str):
        self.log("iteration", {"iteration": iteration, "status": status, "severity": severity})

    def log_redesign(self, redesign_count: int, reason: str):
        self.log("redesign", {"redesign_count": redesign_count, "reason": reason})

