def call_ai_api_with_metrics(
    model_name: str,
    api_key: str,
    prompt: str,
    temperature: float,
    config: APIConfig,
    audit_logger: AuditLogger,
    agent_name: str,
    timeout: int = 300
) -> Tuple[str, float, int]:
    """API call with timeout, retry, cost tracking, audit logging"""

    def api_call():
        # Create the base payload that all models accept
        payload = {
            "model": model_name,
            "messages": [{"role": "user", "content": [{"type": "text", "text": prompt}]}],
            "temperature": 1.0
        }

        # Only add the extra beta parameters if it is NOT an Anthropic model
        if "anthropic" not in model_name.lower():
            payload["top_p"] = 1.0
            payload["n"] = 1
            payload["presence_penalty"] = 0.0
            payload["frequency_penalty"] = 0.0

        response = requests.post(
            url=config.BASE_URL,
            headers={"Authorization": api_key, "Content-Type": "application/json"},
            data=json.dumps(payload),
            timeout=timeout
        )

        if response.status_code != 200:
            raise Exception(f"API Error {response.status_code}: {response.text}")

        response_data = json.loads(response.content)

        if 'choices' in response_data:
            content = response_data['choices'][0]['message']['content']
            tokens = response_data.get('usage', {}).get('total_tokens', len(prompt) // 4 + len(content) // 4)
        else:
            content = response_data.get('content', str(response_data))
            tokens = len(prompt) // 4 + len(content) // 4

        cost_per_token = config.COST_PER_1K_TOKENS.get(model_name, 0.0001) / 1000
        cost = tokens * cost_per_token

        audit_logger.log_api_call(agent_name, model_name, len(prompt), len(content), cost)

        return content, cost, tokens

    try:
        return exponential_backoff_retry(api_call)
    except Exception as e:
        print(f"CRITICAL: API failed: {str(e)}")
        return f"ERROR: {str(e)}", 0.0, 0

