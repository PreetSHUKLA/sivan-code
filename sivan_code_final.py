class ContextManager:
    """Intelligently manage context across iterations"""

    def __init__(self, limits: ContextLimits = None):
        self.limits = limits or ContextLimits()
        self.iteration_history = []

    def estimate_tokens(self, text: str) -> int:
        """Rough token estimation (1 token ≈ 4 chars)"""
        return len(str(text)) // 4

    def truncate_text(self, text: str, max_length: int) -> str:
        """Truncate text to max length"""
        if len(text) <= max_length:
            return text
        return text[:max_length] + "\n... [truncated for brevity]"

    def extract_critical_issues(self, feedback: str) -> str:
        """Extract only CRITICAL issues from review"""

        if not feedback:
            return "No feedback provided."

        # Try to find CRITICAL ISSUES section
        if "CRITICAL ISSUES" in feedback.upper():
            try:
                start = feedback.upper().find("CRITICAL ISSUES")
                end = feedback.upper().find("MEDIUM ISSUES", start)
                if end == -1:
                    end = feedback.upper().find("MINOR ISSUES", start)
                if end == -1:
                    end = len(feedback)

                critical_section = feedback[start:end].strip()
                return critical_section[:self.limits.max_feedback_length]
            except:
                pass

        # Fallback: Return first part of feedback
        return self.truncate_text(feedback, 500)

    def extract_specific_fixes(self, feedback: str) -> str:
        """Extract actionable fix instructions"""

        if not feedback:
            return "No specific fixes provided."

        # Try to find SPECIFIC FIXES section
        if "SPECIFIC FIXES" in feedback.upper():
            try:
                start = feedback.upper().find("SPECIFIC FIXES")
                end = feedback.upper().find("REDESIGN", start)
                if end == -1:
                    end = len(feedback)

                fixes_section = feedback[start:end].strip()
                return fixes_section[:self.limits.max_feedback_length]
            except:
                pass

        return "See critical issues above for what to fix."

    def prepare_iteration_context(
        self,
        task: str,
        plan: str,
        previous_code: Optional[str],
        feedback: Optional[str],
        iteration: int,
        priority_fixes: Optional[str] = None
    ) -> Tuple[str, Dict]:
        """Prepare optimized context for Worker agent"""

        metadata = {
            'iteration': iteration,
            'included_code': False,
            'included_feedback': False,
            'tokens_estimate': 0
        }

        # ITERATION 1: Fresh start
        if iteration == 1:
            prompt = f"""You are an expert Python developer. Implement this coding task.\n\nTASK:\n{task}\n\nIMPLEMENTATION PLAN:\n{self.truncate_text(plan, 2000)}\n\nMANDATORY REQUIREMENTS:\n- Follow the plan architecture EXACTLY\n- Use recommended libraries from the plan\n- Include comprehensive error handling\n- Add type hints and detailed docstrings\n- Handle all edge cases mentioned\n- Write production-ready, secure code\n- NO quick hacks or unsafe patterns\n\nIf using external libraries, add installation comments.\n\nOutput ONLY the Python code wrapped in ```python code blocks.\n"""
            metadata['tokens_estimate'] = self.estimate_tokens(prompt)
            return prompt, metadata

        # ITERATION 2+: Include previous code + focused feedback
        code_to_include = previous_code or ""
        if len(code_to_include) > self.limits.max_code_length:
            code_to_include = self.truncate_text(code_to_include, self.limits.max_code_length)

        metadata['included_code'] = bool(previous_code)

        # Prepare focused feedback
        if priority_fixes:
            focused_feedback = f"PRIORITY FIXES:\n{priority_fixes}"
            metadata['included_feedback'] = True
        elif feedback:
            critical = self.extract_critical_issues(feedback)
            fixes = self.extract_specific_fixes(feedback)
            focused_feedback = f"{critical}\n\n{fixes}"
            metadata['included_feedback'] = True
        else:
            focused_feedback = "No specific feedback - improve code quality."

        focused_feedback = self.truncate_text(focused_feedback, self.limits.max_feedback_length)

        prompt = f"""You are an expert Python developer. REVISE the code based on reviewer feedback.\n\nORIGINAL TASK:\n{task}\n\nPREVIOUS CODE (Iteration {iteration - 1}):\n```python\n{code_to_include}\nISSUES DETECTED:\n{focused_feedback}\n\nINSTRUCTIONS:\n\nFix ALL issues mentioned in the feedback\n\nPrioritize security and correctness over cleverness\n\nUse established libraries where recommended\n\nInclude comments explaining fixes\n\nProvide ONLY the complete, corrected Python code\n\nOutput clean, working Python code wrapped in ```python code blocks.\n"""
        metadata['tokens_estimate'] = self.estimate_tokens(prompt)

        return prompt, metadata

    def log_iteration(self, iteration: int, prompt_tokens: int, code_length: int):
        """Track iteration history"""
        self.iteration_history.append({
            'iteration': iteration,
            'prompt_tokens': prompt_tokens,
            'code_length': code_length
        })

    def get_context_summary(self) -> Dict:
        """Get summary of context usage"""
        if not self.iteration_history:
            return {
                'total_iterations': 0,
                'total_tokens_used': 0,
                'avg_tokens_per_iteration': 0
            }

        total_tokens = sum(h['prompt_tokens'] for h in self.iteration_history)

        return {
            'total_iterations': len(self.iteration_history),
            'total_tokens_used': total_tokens,
            'avg_tokens_per_iteration': total_tokens / len(self.iteration_history),
            'iterations': self.iteration_history
        }
